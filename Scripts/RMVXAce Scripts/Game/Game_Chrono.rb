class Game_Chrono
  #--------------------------------------------------------------------------
  #  Object Initialization
  #--------------------------------------------------------------------------
  def initialize
    @count = 0
    @secondes = 0
    @minutes = 0
    @working = false
  end
  #--------------------------------------------------------------------------
  #  Frame Update
  #--------------------------------------------------------------------------
  def update
      @count += 1
      
      
      if( @count % 60 == 0 )
        @secondes += 1
        
        if( @secondes % 60 == 0 )
             @secondes = 0
             @minutes += 1
        end
      end
     
      
      
  end
  #--------------------------------------------------------------------------
  #  Start
  #--------------------------------------------------------------------------
  def start()
    @working = true
  end
  #--------------------------------------------------------------------------
  #  Stop
  #--------------------------------------------------------------------------
  def stop
    @working = false
  end
  #--------------------------------------------------------------------------
  #  Determine if Working
  #--------------------------------------------------------------------------
  def working
    @working
  end
  
  #--------------------------------------------------------------------------
  #  Get Count
  #--------------------------------------------------------------------------
  def count
    @count
  end
  #--------------------------------------------------------------------------
  #  Get Seconds
  #--------------------------------------------------------------------------
  def sec
    return @secondes
  end
  
  #--------------------------------------------------------------------------
  #  Get Seconds
  #--------------------------------------------------------------------------
  def min
    return @minutes
  end
end
