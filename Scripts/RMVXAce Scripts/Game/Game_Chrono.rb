#==============================================================================
#  Game_Chrono
#------------------------------------------------------------------------------
#  Class permettant de faire la gestion d'un chronomètre, ou on part de 0 sec
#  et le temps incrémente jusqu'a se qu'on l'arrête. Contraire de Game_Timer
#==============================================================================

class Game_Chrono
  #--------------------------------------------------------------------------
  #  Object Initialization
  #--------------------------------------------------------------------------
  def initialize
    @count = 0
    @working = false
  end
  #--------------------------------------------------------------------------
  #  Frame Update
  #--------------------------------------------------------------------------
  def update
      @count += 1
  end
  #--------------------------------------------------------------------------
  #  Start
  #--------------------------------------------------------------------------
  def start()
    @working = true
  end
  #--------------------------------------------------------------------------
  #  Stop
  #--------------------------------------------------------------------------
  def stop
    @working = false
  end
  #--------------------------------------------------------------------------
  #  Determine if Working
  #--------------------------------------------------------------------------
  def working
    @working
  end
  
  #--------------------------------------------------------------------------
  #  Get Count
  #--------------------------------------------------------------------------
  def count
    @count
  end
  #--------------------------------------------------------------------------
  #  Get Seconds
  #--------------------------------------------------------------------------
  def sec
    return 60 #(@count  Graphics.frame_rate) % 60
  end
  
  #--------------------------------------------------------------------------
  #  Get Seconds
  #--------------------------------------------------------------------------
  def min
    return 60 #(@count  Graphics.frame_rate) %  60
  end
end
